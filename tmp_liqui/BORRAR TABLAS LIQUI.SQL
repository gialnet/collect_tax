SELECT I.AYTO,PADRON,YEAR,PERIODO,I.VOL_EJE,
	   SUM(I.PRINCIPAL),SUM(I.RECARGO),SUM(I.DEMORA),SUM(I.COSTAS)
FROM INGRESOS I, VALORES V
WHERE TRUNC(I.FECHA,'DD') BETWEEN '01/08/2002' AND '31/08/2002'
AND I.NUMERO_DE_DATA IS NOT NULL
AND I.AYTO NOT LIKE 'J%' AND I.VALOR=V.ID
GROUP BY I.AYTO,PADRON,YEAR,PERIODO,I.VOL_EJE;


